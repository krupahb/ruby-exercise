puts "enter the day"
day=gets.chomp

case day
when  "sunday"
	puts "banuvara"

when  "monday"
	puts "somavara"
when  "tuesday"
	puts "magalavara"
when "wednesday"
	puts "budavara"
when  "thursday"
	puts "guruvara"
when  "friday"
	puts "shukravara"
when  "saturday"
	puts "shanivara"
else 
	puts "undefined"
end